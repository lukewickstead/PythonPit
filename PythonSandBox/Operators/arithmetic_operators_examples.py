# Arithmetic Operators Example

print("1 + 1 =", 1 + 1)             # Addition
print("2 - 1 =", 2 - 1)             # Subtraction
print("2 * 3 =", 2 * 3)             # Multiplication
print("2 ** 3 =", 2 ** 3)           # Power of
print("10 / 3 =", 10 / 3)           # Int division ( returns result and not truncates to int
print("10 / 3.1 =", 10 / 3.1)       # float division
print("10 // 3.1 =", 10 // 3.1)     # floor division ( as per int division )
print("10 % 3 = ", 10 % 3)          # Modulus