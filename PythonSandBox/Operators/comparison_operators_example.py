# Comparison Operators Example

print("1 == 1 =", 1 == 1)  # Equality
print("1 != 1 =", 1 != 2)  # Inequality
print("2 > 1 =", 2 > 1)  # Greater than
print("1 < 1 =", 1 < 2)  # Less than
print("1 >= 1 =", 1 >= 1)  # Greater than or equal to
print("1 <= 1 =", 1 <= 1)  # Less than or equal to
