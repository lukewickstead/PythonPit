# Assignment Operators Example

value = 10
print("value = 1:", value)          # Assignment

value += 2
print("value += 2:", value)         # Addition and assignment

value *= 3
print("value *= 2:", value)         # Multiplication and assignment

value /= 3
print("value /= 3:", value)         # Division and assignment

value %= 5
print("value %= 5:", value)         # Modulus and assignment

value **= 2
print("value **= 2:", value)        # Power of and assignment

value //= 3
print("value //= 3:", value)        # Floor division ( int division ) and assignment